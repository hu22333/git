#python使用加号(+)来合并字符串，合并字符串的方式称为拼接
first_name = "ada"
last_name = "lovelace"
full_name = first_name+" "+last_name
print(full_name)
#可以使用titile()来设置合适的格式
first_name="ada"
last_name="lovelace"
full_name=first_name+" "+last_name
print("hello, "+full_name.title()+"!")
